# --- STEP 10: TEXT ---
def analyze_text_emotion(text):
    if not text.strip():
        return "Please enter text."

    try:
        results = text_classifier(text)[0]

        results = sorted(results, key=lambda x: x['score'], reverse=True)

        output = ""
        for item in results[:5]:
            output += f"{item['label']}: {item['score']:.3f}\n"

        save_to_drive("text", output, "user")
        return output

    except Exception as e:
        return f"Text Error: {str(e)}"

# --- STEP 11: IMAGE ---
def analyze_image_emotion(image):
    if image is None:
        return "Please upload image."

    try:
        temp_path = "/content/temp_img.jpg"
        image.save(temp_path)

        result = DeepFace.analyze(
            img_path=temp_path,
            actions=['emotion'],
            enforce_detection=False
        )

        emotions = result[0]["emotion"]

        sorted_emotions = sorted(
            emotions.items(),
            key=lambda x: x[1],
            reverse=True
        )

        output = ""
        for emotion, score in sorted_emotions[:5]:
            output += f"{emotion}: {score:.2f}\n"

        save_to_drive("image", output, "user")
        return output

    except Exception as e:
        return f"Image Error: {str(e)}"
