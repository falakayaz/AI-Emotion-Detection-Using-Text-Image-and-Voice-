def analyze_audio_emotion(audio_file):
    if audio_file is None:
        return "Please upload audio."

    try:
        features = extract_audio_features(audio_file)
        features = np.array(features).reshape(1, -1)

        # =========================
        # ✅ TRAINED MODEL MODE
        # =========================
        if model is not None and encoder is not None:
            probs = model.predict_proba(features)[0]

            results = sorted(
                [(encoder.inverse_transform([i])[0], float(p)) for i, p in enumerate(probs)],
                key=lambda x: x[1],
                reverse=True
            )

        # =========================
        # ❌ FALLBACK MODE (IMPROVED)
        # =========================
        else:
            f = features.flatten()

            # simple heuristic scoring (more stable than energy-only)
            scores = {
                "angry": np.mean(f[:5]),
                "happy": np.mean(f[5:10]),
                "sad": np.mean(f[10:15]),
                "neutral": np.mean(f[15:20]),
                "fearful": np.mean(f[20:25]),
                "disgust": np.mean(f[25:30]),
                "surprised": np.mean(f[30:35]),
                "calm": np.mean(f[35:40])
            }

            total = sum(abs(v) for v in scores.values())
            results = [(k, abs(v)/total if total != 0 else 0.12) for k, v in scores.items()]
            results = sorted(results, key=lambda x: x[1], reverse=True)

        # =========================
        # OUTPUT FORMAT
        # =========================
        output = "🎧 Audio Emotion Prediction Results:\n"
        for emo, score in results[:8]:
            output += f"{emo.upper()}: {score:.2f}\n"

        save_to_drive("audio", output, "user")
        return output

    except Exception as e:
        return f"Audio Error: {str(e)}"
